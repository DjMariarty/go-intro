package main

import "fmt"

func main() {

	// Zero value — это значение, которое автоматически присваивается переменной, если она объявлена, но не инициализирована.
	//  Для разных типов данных нулевые значения отличаются

	var num1 int32

	var num2 int64

	var num3 int

	var num4 float32

	var num5 float64

	var num6 string

	// вывод в консоль
	fmt.Println(num1)
	fmt.Println(num2)
	fmt.Println(num3)
	fmt.Println(num4)
	fmt.Println(num5)
	fmt.Println(num6)

}

